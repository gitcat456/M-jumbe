class Issue {
  final String title;
  final String description;
  final String location;
  final String status;
  final String category;
  final String time;
  final String date;
  final String ward;
  final String imageUrl;

  Issue({
    required this.title,
    required this.description,
    required this.location,
    required this.status,
    required this.date,
    required this.ward,
    required this.category,
    required this.time,
    required this.imageUrl,
  });
}
